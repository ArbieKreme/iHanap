      <h1>About iHanap</h1>
      <h5 class="w3-padding-32">The iHanap: A mobile Application for Missing Person is designed and conducted in order to provide a reliable and convenient service for the Filipino who needs help and support in finding their loved ones.</h5>

      <p class="w3-text-grey">The iHanap, is a web-based mobile application that enables a person to report missing person, search for a missing person and track the progress of their report from the Police Station to provide aid and assistance while the filed report for their missing loved is still in progress.</p>
      <p class="w3-text-grey">The main objective in creating the iHanap Mobile Application is to create a platform for the Philippine National Police in cascading their operations in finding missing persons and also to serve as a medium for the Filipino citizens who have been finding their loved ones. Technology has played a big role in this generation, the iHanap will serve as a stepping stone in the development of operations of the National Police in providing quality service for the Filipinos.</p>
